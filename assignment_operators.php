<?php

// Assignment operators

// =
$a = 10;
echo "The value of a is {$a} <br>";

// ++
$a = 10;
$a++; //$a = $a + 1;
echo "The value of a is {$a} <br>";

// --
$a = 10;
$a--; // $a = $a - 1;
echo "The value of a is {$a} <br>";

// --
$a = 10;
$a += 3; // $a = $a + 3;
echo "The value of a is {$a} <br>";

// --
$a = 10;
$a -= 3; // $a = $a - 3;
echo "The value of a is {$a} <br>";
