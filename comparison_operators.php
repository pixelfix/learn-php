<?php

// Comparison operators

// == Equal
$a = 10;
$b = "10";
echo "a == b ";
var_dump($a == $b);
echo "<br>";

// === Identical
$a = 10;
$b = 10;
echo "a === b ";
var_dump($a === $b);
echo "<br>";

// != Not equal
$a = 10;
$b = 5;
echo "a != b ";
var_dump($a != $b);
echo "<br>";

// !== Not identical
$a = 10;
$b = 10;
echo "a !== b ";
var_dump($a !== $b);
echo "<br>";

// > Bigger than
$a = 10;
$b = 5;
echo "a > b ";
var_dump($a > $b);
echo "<br>";

// >= Bigger or equal than
$a = 10;
$b = 10;
echo "a >= b ";
var_dump($a >= $b);
echo "<br>";

// < Less than
$a = 10;
$b = 5;
echo "a < b ";
var_dump($a < $b);
echo "<br>";

// <= Less or equal than
$a = 10;
$b = 5;
echo "a <= b ";
var_dump($a < $b);
echo "<br>";
