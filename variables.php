<?php

// What is variable?
// A variable is an entity with a unique name, and as the name suggests the value can vary

// string
$name = "Pixelfix";
echo "Hello, I am from {$name} <br>";

// integer
$age = 21;
echo "My age is {$age} <br>";

// float
$interestRate = 5.55;
echo "My interest rate is {$interestRate} <br>";

// boolean
$likeBurgers = false;
echo "Do I like burgers: {$likeBurgers}";
